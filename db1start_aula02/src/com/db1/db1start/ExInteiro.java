package com.db1.db1start;

public class ExInteiro {
	public static int soma(Integer numero1, Integer numero2) {
		Integer resultado;
		resultado = numero1 + numero2;
		return resultado;
	}

	public static int sub(Integer numero1, Integer numero2) {
		Integer resultado;
		resultado = numero1 - numero2;
		return resultado;
	}
}